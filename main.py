# main.py

def add(a, b):
    """
    Function to add two numbers.
    """
    return a + b

def subtract(a, b):
    """
    Function to subtract two numbers.
    """
    return a - b

if __name__ == "__main__":
    # This is just a simple way to use the functions if the script is run directly
    print("Addition of 1 and 2 is:", add(1, 2))
    print("Subtraction of 3 and 1 is:", subtract(3, 1))

